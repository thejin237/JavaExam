package com.jslhrd.db;

public class BooklentDTO {
	private int lenno;
	private int usemo;
	private int bookno;
	private String outdate;
	private String indate;
	public int getLenno() {
		return lenno;
	}
	public void setLenno(int lenno) {
		this.lenno = lenno;
	}
	public int getUsemo() {
		return usemo;
	}
	public void setUsemo(int usemo) {
		this.usemo = usemo;
	}
	public int getBookno() {
		return bookno;
	}
	public void setBookno(int bookno) {
		this.bookno = bookno;
	}
	public String getOutdate() {
		return outdate;
	}
	public void setOutdate(String outdate) {
		this.outdate = outdate;
	}
	public String getIndate() {
		return indate;
	}
	public void setIndate(String indate) {
		this.indate = indate;
	}
	
}
