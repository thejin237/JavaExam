public class Exam_02 {
	public static void main(String[] args) {
		int sw = 7;
		switch(sw) {
		case 1:
		case 2:
			System.out.println("1+2");
			break;
		case 3:
		case 4:
			System.out.println("4-3");
			break;
		case 5:
		case 6:
			System.out.println("5*6");
			break;
		default: 
			System.out.println("NO");
		}
	}
}
